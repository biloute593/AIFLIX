# Commandes à exécuter après avoir créé le repository GitHub
# Remplacez VOTRE_USERNAME par votre nom d'utilisateur GitHub

# 1. Ajouter le remote origin
git remote add origin https://github.com/VOTRE_USERNAME/aiflix.git

# 2. Pousser le code vers GitHub
git push -u origin master

# 3. Vérifier que c'est bien poussé
git remote -v